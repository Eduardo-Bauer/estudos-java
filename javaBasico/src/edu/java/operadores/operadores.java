package edu.java.operadores;

public class Operadores {
    public static void main(String[] args) {
        // atribuicao: =
        int idade0 = 10;

        // adicao: +
        int idade1 = 20 + 10;
        idade1++;
        String texto = "Ola" + " " + "Mundo";

        // subtracao: -
        int idade2 = 20 - 10;
        idade2--;
        idade2 = - idade2;

        // multiplicacao: *
        int idade3 = 20 * 10;

        // divisao: /
        int idade4 = 20 / 10;

        // inversao: !
        boolean variavel = true;
        variavel = ! variavel;

        // operador ternario: ?
        String resultado = 6 == 6 ? "verdadeiro" : "falso";

        /* operadores relacionais:
        * igualdade: ==
        * diferenciacao: !=
        * maioridade: >
        * maioridade ou igualdade: >=
        * menoriedade: <
        * menoriedade ou igualdade: <=
        * comparacao de objetos de igualdade: equals;
        * */

        /* operadores logicos:
        * E: &&
        * OU: ||
        * */
    }
}
